"""
Ejercicio 2
Ingresar una vocal y decir si es 1,2,3,4 o 5 el lugar que ocupa.

"""

vocal = input("Ingrese vocal [a, e, i, o y u]: ")
if  vocal == "a":
    print("1")
elif vocal == "e":
    print("2")
elif vocal == "i":
    print("3")
elif vocal == "o":
    print("4")
else:
    print("5")